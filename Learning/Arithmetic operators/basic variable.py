
# Variable declaration and assignment
integer_var = 10  # Integer variable
float_var = 3.14  # Float variable
string_var = "Hello, World!"  # String variable
boolean_var = True  # Boolean variable
list_var = [1, 2, 3]  # List variable
tuple_var = (4, 5, 6)  # Tuple variable
set_var = {7, 8, 9}  # Set variable
dict_var = {"name": "John", "age": 25}  # Dictionary variable

# Print the values of the variables
print("Integer variable:", integer_var)
print("Float variable:", float_var)
print("String variable:", string_var)
print("Boolean variable:", boolean_var)
print("List variable:", list_var)
print("Tuple variable:", tuple_var)
print("Set variable:", set_var)
print("Dictionary variable:", dict_var)

# Data type conversion
integer_to_float = float(integer_var)  # Convert integer to float
float_to_integer = int(float_var)  # Convert float to integer
integer_to_string = str(integer_var)  # Convert integer to string
boolean_to_string = str(boolean_var)  # Convert boolean to string

# Print the converted values
print("Integer to float:", integer_to_float)
print("Float to integer:", float_to_integer)
print("Integer to string:", integer_to_string)
print("Boolean to string:", boolean_to_string)

# Hexadecimal number
hex_number = 0x1A  # Hexadecimal number (26 in decimal)

# Complex number
complex_number = 2 + 3j  # Complex number with real and imaginary parts

# Print the values of the hexadecimal and complex numbers
print("Hexadecimal number:", hex_number)
print("Complex number:", complex_number)
