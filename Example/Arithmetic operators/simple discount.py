# Harga barang sebelum diskon
harga_barang = 100000

# Persentase diskon
diskon = 20

# Menghitung harga setelah diskon
harga_setelah_diskon = harga_barang - (harga_barang * diskon / 100)

# Menampilkan harga setelah diskon
print("Harga setelah diskon:", harga_setelah_diskon)
# Price of the item before discount
item_price = 100000

# Discount percentage
discount = 20

# Calculating the price after discount
discounted_price = item_price - (item_price * discount / 100)

# Displaying the price after discount
print("Price after discount:", discounted_price)
